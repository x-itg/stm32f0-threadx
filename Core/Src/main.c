#define MAXRXU2 100
unsigned short RxLenU2 = 0;
unsigned char RxBufU2[MAXRXU2];


void u2rxstrdecodeProcess(void)
{

  if ((__HAL_UART_GET_FLAG(&huart2, UART_FLAG_IDLE) != RESET)) //空闲中断
  {
    __HAL_UART_CLEAR_IDLEFLAG(&huart2);
    HAL_UART_DMAStop(&huart2);

    __HAL_UART_CLEAR_FLAG(&huart2, UART_CLEAR_OREF | UART_CLEAR_NEF | UART_CLEAR_PEF | UART_CLEAR_FEF); //在可能接受到大量数据的时候需要清除这些标志

    RxLenU2 = MAXRXU2 - huart2.hdmarx->Instance->CNDTR; //接收到的字节长度

    HAL_GPIO_WritePin(USART2_DE_GPIO_Port,USART2_DE_Pin,GPIO_PIN_SET);//切换成发送
      
    HAL_UART_Transmit(&huart2, RxBufU2, RxLenU2,100);
    
    HAL_GPIO_WritePin(USART2_DE_GPIO_Port,USART2_DE_Pin,GPIO_PIN_RESET);//接换成接收
      
    HAL_UART_Receive_DMA(&huart2, RxBufU2, MAXRXU2); //再次开启DMA接收
  }
}
